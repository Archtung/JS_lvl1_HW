// Задание 5. Реализовать основные 4 арифметические операции в виде функций с двумя параметрами. Обязательно использовать оператор return.

function plus(c, g) {
    return c + g;
}

function minus(c, g) {
    return c - g;
}

function div(c, g) {
    return c / g;
}

function mult(c, g) {
    return c * g;
}
console.log("---------------Задание 5----------------")

console.log("Сумма = ",plus(2,4))
console.log("Вычитание = ",minus(5,2))
console.log("Деление = ",div(6,3))
console.log("Умножение = ",mult(4,8))
